  Widget buildRadioOption(String value) {
    return Row(
      children: [
        Radio<String>(
          activeColor: mainBlue,
          value: value,
          groupValue: academicYear,
          onChanged: (val) => setState(() => academicYear = val),
        ),
        Text(value, style: radioTextStyle),
      ],
    );
  }

